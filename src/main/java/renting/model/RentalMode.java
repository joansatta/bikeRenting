package renting.model;

public class RentalMode {

	public Integer price;
	public Integer rentalPeriodInHours;
	
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	public Integer getRentalPeriodInHours() {
		return rentalPeriodInHours;
	}
	public void setRentalPeriodInHours(Integer rentalPeriodInHours) {
		this.rentalPeriodInHours = rentalPeriodInHours;
	}
	
	
}
